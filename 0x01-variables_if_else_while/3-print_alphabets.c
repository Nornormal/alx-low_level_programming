#include <stdio.h>
#include <stdlib.h>

/**
 * main - print alphabet in lowercase and uppercase
 *
 * Return: Always 0 success
 */

int main(void)
{
	char e, f;
	int c, m;

	e = 'a';
	f = 'A';
	while (e <= 'z')
	{
		putchar('e');
		c = c + 1;
	}
	while (f <= 'Z')
	{
		putchar('f');
		m = m + 1;
	}
	putchar('\n');
	return (0);
}
